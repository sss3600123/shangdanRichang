<?php

/* --------------------------------------------------------------------------*/
/**
* @tianxiaolong  
*
* @Param 2.4
*
* @Returns   
* round() 四舍五入
* ceil() 上取整 
* floor() 去小数位
 */
/* ----------------------------------------------------------------------------*/
$number = round(2.4);
printf("2.4 rounds to the float %s<br/>", $number);

$n2 = ceil(2.4);
printf("2.4 rounds up to the float %s<br/>", $n2);

$n3 = floor(2.6);
printf("2.6 rounds down to the float %s<br/>", $n3);

$n4 = round(-2.6);
printf("-2.4 rounds to the float %s<br/>", $n4);
