function yh2()
{
	for($i = 1;$i < 20; $i++)
	{
		$arr[$i][1] = 1;
		for($j = 1; $j <= $i; $j++)
		{
			if($i == $j)
			{
				$arr[$i][$j] = 1;
			}
			else
			{
				if(!isset($arr[$i-1][$j-1]))
				{
					$arr[$i-1][$j-1] = 0;
				}

				
				$arr[$i][$j] = $arr[$i-1][$j-1] + $arr[$i-1][$j];					
				
			
			}
			echo $arr[$i][$j]." ";					
		}
		echo "<br/>";
	}
}
