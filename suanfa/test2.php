/**
* @tianxiaolong  
* 牛年求牛：有一母牛，到4岁可生育，每年一头，所生均是一样的母牛，
* 到15岁绝育，不再能生，20岁死亡，问n年后有多少头牛
* @Returns   
 */
/* ----------------------------------------------------------------------------*/
function shengN($n)
{
	$num = 1;
	
	for($j = 1;$j <= $n;$j++)
	{
		if($j > 4 && $j < 15)
		{
			++$num;	
			shengN($n-$j);		
		}

		if($j >= 20)
		{
			--$num;
		}
			
	}
	return $num;
}

echo shengN(50);


